package week05.evening;

public class ConcatAndAddition {

    public static void main(String[] args) {
        char ch='e';
        char ch1='d';

        int data1=ch;
        int data2=ch1;

        System.out.println("data1 = " + data1); //101
        System.out.println("data2 = " + data2); //100
        String stringData=""+ch+ch1;
        System.out.println("stringData = " + stringData);

    }
}
